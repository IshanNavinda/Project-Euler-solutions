# fuction for check num is prime------------------------------------------------
#-------------------------------------------------------------------------------
def prime_check(p):

    if p < 2:
        return False

    for i in range(2, int(p**0.5)+1):
        if p % i == 0:
            return False

    return True
#-------------------------------------------------------------------------------


# function for check num is trucable num----------------------------------------
#-------------------------------------------------------------------------------
def truncable_prime_find(t):

    if t < 10:
        return False

    num = str(t)

    for i in range(len(num)):
        if not prime_check(int(num[i:])):
            return False
        
        if not prime_check(int(num[:i+1])):
            return False
    
    return True
#-------------------------------------------------------------------------------


#fuction for get first 11 truncable num to a list-------------------------------
#-------------------------------------------------------------------------------
def last_step():

    all_list = []
    temp_num = 11

    while len(all_list) < 11:
        if prime_check(temp_num) and truncable_prime_find(temp_num):
            all_list.append(temp_num)

        temp_num+=2

    print(sum(all_list))
#-------------------------------------------------------------------------------

# call fuction------------------------------------------------------------------
#-------------------------------------------------------------------------------
last_step()
